package com.syntax.class07;

public class incrementDecrement {

	public static void main(String[] args) {
		int num = 0;
		num=num+1;
		num+=1;
		
		System.out.println(num);
		
		num++;//adding 1
		System.out.println(num);
		
		int num1=100;
		
		num1-=1;
		num1--;//subtract 1
		
		//increment & decrement operators can be used ONLY with variables
		
		System.out.println("-------------------------------------");
		
		System.out.println ("Hello");
		
		
		
		
		
	}

}
